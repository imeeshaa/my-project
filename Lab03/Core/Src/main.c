/**
 * @brief Display a hexadecimal digit (0-F) on seven segment display
 * @param num: Number to display (0-15)
 * @retval None
 */
void display_number(uint8_t num) {
    // Ensure input is within valid range
    if (num > 15) {
        return;
    }
    
    // Clear all segments first
    clear_display();
    
    // Get the pattern for the digit
    uint8_t pattern = hex_patterns[num];
    
    // Set individual segments based on pattern
    if (pattern & 0x01) HAL_GPIO_WritePin(SEG_PORT, SEG_A_PIN, GPIO_PIN_SET);
    if (pattern & 0x02) HAL_GPIO_WritePin(SEG_PORT, SEG_B_PIN, GPIO_PIN_SET);
    if (pattern & 0x04) HAL_GPIO_WritePin(SEG_PORT, SEG_C_PIN, GPIO_PIN_SET);
    if (pattern & 0x08) HAL_GPIO_WritePin(SEG_PORT, SEG_D_PIN, GPIO_PIN_SET);
    if (pattern & 0x10) HAL_GPIO_WritePin(SEG_PORT, SEG_E_PIN, GPIO_PIN_SET);
    if (pattern & 0x20) HAL_GPIO_WritePin(SEG_PORT, SEG_F_PIN, GPIO_PIN_SET);
    if (pattern & 0x40) HAL_GPIO_WritePin(SEG_PORT, SEG_G_PIN, GPIO_PIN_SET);
}

/**
 * @brief Clear all segments of the display
 * @param None
 * @retval None
 */
void clear_display(void) {
    HAL_GPIO_WritePin(SEG_PORT, ALL_SEGMENTS, GPIO_PIN_RESET);
}
